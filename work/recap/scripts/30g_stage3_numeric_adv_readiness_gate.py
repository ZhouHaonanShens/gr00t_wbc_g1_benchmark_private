#!/usr/bin/env python3

from __future__ import annotations

import argparse
import json
import math
import sys
from pathlib import Path
from typing import Any


REPO_ROOT = Path(__file__).resolve().parents[3]
if str(REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(REPO_ROOT))


from work.recap.scripts.state_conditioned_common import read_json
from work.recap.scripts.state_conditioned_common import write_json
from work.recap.stage3_contract_precondition_gate import ADVANTAGE_BIAS_KEY
from work.recap.stage3_contract_precondition_gate import ADVANTAGE_WEIGHT_KEY
from work.recap.stage3_contract_precondition_gate import BASELINE_DEFAULT_ADV_INIT
from work.recap.stage3_contract_precondition_gate import (
    run_stage3_contract_precondition_gate,
)


DEFAULT_ITERATION_MANIFEST = Path(
    "agent/artifacts/stage3_iteration/recap_stage3_iter_002/iteration_manifest.json"
)
DEFAULT_BASELINE_CHECKPOINT = Path(
    "agent/artifacts/stage3_t3b_baseline_1gpu/formal_run/checkpoint-200"
)
DEFAULT_ADVANTAGE_MANIFEST = Path(
    "agent/artifacts/recap_min_loop/single_gpu_v1/t8_numeric_advantage/advantage_label_manifest.json"
)
DEFAULT_OUTPUT_JSON = Path(
    "agent/artifacts/recap_min_loop/single_gpu_v1/t9_numeric_advantage_pretrain_gate.json"
)
SCHEMA_VERSION = "stage3_numeric_advantage_pretrain_gate_v1"
ARTIFACT_KIND = "stage3_numeric_advantage_pretrain_gate"
CONDITIONED_TRAINING_ENTRYPOINT = "work/recap/scripts/34b_recap_numeric_adv_smoke.py"


def _resolve_path(raw: str | Path) -> Path:
    path = Path(str(raw)).expanduser()
    if not path.is_absolute():
        path = REPO_ROOT / path
    return path.resolve()


def _repo_relative(path: Path | str) -> str:
    resolved = _resolve_path(path)
    try:
        return str(resolved.relative_to(REPO_ROOT))
    except ValueError:
        return str(resolved)


def _read_json_object(path: Path) -> dict[str, Any]:
    payload = read_json(path)
    if not isinstance(payload, dict):
        raise TypeError(f"expected JSON object at {path}, got {type(payload).__name__}")
    return {str(key): value for key, value in payload.items()}


def _stats_are_finite(stats_payload: Any) -> bool:
    if not isinstance(stats_payload, dict):
        return False
    for value in stats_payload.values():
        if not isinstance(value, (int, float)):
            return False
        if not math.isfinite(float(value)):
            return False
    return True


def _resolved_checkpoint_matches_requested(
    *, contract_gate_payload: dict[str, Any], requested_checkpoint: Path
) -> bool:
    features_raw = contract_gate_payload.get("checkpoint_weight_map_features")
    if not isinstance(features_raw, dict):
        return False
    checkpoint_path_raw = features_raw.get("checkpoint_path")
    if not isinstance(checkpoint_path_raw, str) or not checkpoint_path_raw.strip():
        return False
    return _resolve_path(checkpoint_path_raw) == requested_checkpoint


def _conditioned_training_contract(checkpoint_path: Path) -> dict[str, Any]:
    target_keys = [ADVANTAGE_WEIGHT_KEY, ADVANTAGE_BIAS_KEY]
    return {
        "entrypoint_script": CONDITIONED_TRAINING_ENTRYPOINT,
        "continuation_checkpoint_path": _repo_relative(checkpoint_path),
        "required_cli_flags": [
            "--condition-focused-continuation",
            "--continuation-checkpoint-path",
            "--tune-projector",
            "--tune-diffusion-model",
        ],
        "trainable_advantage_parameter_keys": list(target_keys),
        "will_create_or_update_advantage_pair": True,
    }


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        prog="30g_stage3_numeric_adv_readiness_gate.py",
        description=(
            "Combine the stage3 contract precondition gate, numeric-advantage label manifest, "
            "and conditioned-training expectation into a single pretrain readiness gate."
        ),
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    parser.add_argument(
        "--iteration-manifest",
        type=Path,
        default=DEFAULT_ITERATION_MANIFEST,
        help="Stage3 iteration manifest already bound to the formal baseline checkpoint.",
    )
    parser.add_argument(
        "--baseline-checkpoint",
        type=Path,
        default=DEFAULT_BASELINE_CHECKPOINT,
        help="Expected formal baseline checkpoint-200 path.",
    )
    parser.add_argument(
        "--advantage-manifest",
        type=Path,
        default=DEFAULT_ADVANTAGE_MANIFEST,
        help="Numeric-advantage manifest generated by Task 11a.",
    )
    parser.add_argument(
        "--output-json",
        type=Path,
        default=DEFAULT_OUTPUT_JSON,
        help="Where to atomically write the pretrain readiness gate JSON.",
    )
    return parser


def build_gate_payload(
    *,
    iteration_manifest: Path,
    baseline_checkpoint: Path,
    advantage_manifest: Path,
    output_json: Path,
) -> dict[str, Any]:
    contract_gate_payload = run_stage3_contract_precondition_gate(
        repo_root=REPO_ROOT,
        manifest_path=iteration_manifest,
    )
    baseline_surface_raw = contract_gate_payload.get("prelim_eval_surface")
    baseline_surface = (
        dict(baseline_surface_raw) if isinstance(baseline_surface_raw, dict) else {}
    )
    baseline_surface_mode = str(baseline_surface.get("mode") or "")
    baseline_surface_allowed = bool(
        baseline_surface_mode == BASELINE_DEFAULT_ADV_INIT
        or contract_gate_payload.get("allow_baseline_default_advantage_embedding_init")
    )

    advantage_manifest_exists = advantage_manifest.is_file()
    numeric_manifest_payload: dict[str, Any] = {}
    if advantage_manifest_exists:
        numeric_manifest_payload = _read_json_object(advantage_manifest)
    numeric_advantage_labels_all_finite = bool(
        numeric_manifest_payload.get("all_labels_finite") is True
    )
    numeric_advantage_stats_all_finite = _stats_are_finite(
        numeric_manifest_payload.get("advantage_stats")
    )
    numeric_advantage_labels_exist = bool(
        advantage_manifest_exists
        and numeric_advantage_labels_all_finite
        and numeric_advantage_stats_all_finite
    )

    checkpoint_matches_requested = _resolved_checkpoint_matches_requested(
        contract_gate_payload=contract_gate_payload,
        requested_checkpoint=baseline_checkpoint,
    )
    conditioned_training = _conditioned_training_contract(baseline_checkpoint)
    conditioned_training_will_create_advantage_pair = bool(
        conditioned_training["will_create_or_update_advantage_pair"]
    )

    failure_reason_codes: list[str] = []
    if not bool(contract_gate_payload.get("pass")):
        failure_reason_codes.extend(
            str(code) for code in contract_gate_payload.get("failure_reason_codes", [])
        )
    if not checkpoint_matches_requested:
        failure_reason_codes.append("manifest_checkpoint_mismatch")
    if not baseline_surface_allowed:
        failure_reason_codes.append("baseline_surface_not_allowed")
    if not numeric_advantage_labels_exist:
        failure_reason_codes.append("numeric_advantage_labels_missing_or_nonfinite")
    if not conditioned_training_will_create_advantage_pair:
        failure_reason_codes.append("conditioned_training_advantage_pair_not_asserted")

    deduped_failure_reason_codes: list[str] = []
    for code in failure_reason_codes:
        if code and code not in deduped_failure_reason_codes:
            deduped_failure_reason_codes.append(code)

    gate_pass = not deduped_failure_reason_codes
    return {
        "schema_version": SCHEMA_VERSION,
        "artifact_kind": ARTIFACT_KIND,
        "iteration_manifest_path": _repo_relative(iteration_manifest),
        "baseline_checkpoint_path": _repo_relative(baseline_checkpoint),
        "advantage_manifest_path": _repo_relative(advantage_manifest),
        "output_json_path": _repo_relative(output_json),
        "contract_precondition_gate": {
            "pass": bool(contract_gate_payload.get("pass")),
            "status": str(contract_gate_payload.get("status") or ""),
            "gate_json_path": str(contract_gate_payload.get("gate_json_path") or ""),
            "failure_reason_codes": list(
                contract_gate_payload.get("failure_reason_codes") or []
            ),
        },
        "baseline_surface_mode": baseline_surface_mode,
        "baseline_surface_selection_reason": str(
            baseline_surface.get("selection_reason") or ""
        ),
        "baseline_surface_allowed_for_pretrain": bool(baseline_surface_allowed),
        "baseline_surface_allow_baseline_default_advantage_embedding_init": bool(
            contract_gate_payload.get("allow_baseline_default_advantage_embedding_init")
        ),
        "manifest_checkpoint_matches_requested_baseline": bool(
            checkpoint_matches_requested
        ),
        "numeric_advantage_manifest_exists": bool(advantage_manifest_exists),
        "numeric_advantage_labels_all_finite": bool(
            numeric_advantage_labels_all_finite
        ),
        "numeric_advantage_stats_all_finite": bool(
            numeric_advantage_stats_all_finite
        ),
        "numeric_advantage_labels_exist": bool(numeric_advantage_labels_exist),
        "numeric_advantage_label_source_mode": str(
            numeric_manifest_payload.get("label_source_mode") or ""
        ),
        "conditioned_training_contract": conditioned_training,
        "conditioned_training_will_create_advantage_pair": bool(
            conditioned_training_will_create_advantage_pair
        ),
        "pass": bool(gate_pass),
        "status": "continue" if gate_pass else "blocked",
        "failure_reason_codes": deduped_failure_reason_codes,
        "exit_code": 0 if gate_pass else 1,
    }


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    iteration_manifest = _resolve_path(args.iteration_manifest)
    baseline_checkpoint = _resolve_path(args.baseline_checkpoint)
    advantage_manifest = _resolve_path(args.advantage_manifest)
    output_json = _resolve_path(args.output_json)
    payload = build_gate_payload(
        iteration_manifest=iteration_manifest,
        baseline_checkpoint=baseline_checkpoint,
        advantage_manifest=advantage_manifest,
        output_json=output_json,
    )
    output_json.parent.mkdir(parents=True, exist_ok=True)
    _ = write_json(output_json, payload)
    print(json.dumps(payload, ensure_ascii=True, indent=2, sort_keys=True))
    return int(payload.get("exit_code", 1))


if __name__ == "__main__":
    raise SystemExit(main())
